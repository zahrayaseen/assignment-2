"use strict";
function checknumber(num) {
    let number = -8;
    if (number == 0) {
        console.log("this number is zero");
    }
    if (number < 0) {
        console.log("this number is negative");
    }
    if (number > 0) {
        console.log("this number is positive");
    }
}
checknumber(5);
